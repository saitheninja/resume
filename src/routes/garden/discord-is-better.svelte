<h1 class="mb-10">Discord is Better Than What We Had Before</h1>

<div class="space-y-4">
  <p>
    I don't want to look like a asshole. Knowing my messages eventually get
    deleted relieves some of my posting anxiety. Especially when I ask something
    obvious or dumb: that stuff is just clutter. (Memes go stale incredibly
    quickly). Discord vs forums is like texting vs email.
  </p>

  <p>
    Sure, It doesn't take up much space to store messages, but the internet is
    rotting and those forums aren't immune (especially the images, which usually
    had to be hosted separately and are therefore long gone).
  </p>

  <p>
    I've definitely found value in decades old forum threads, however I also
    find value in all the nice features Discord has. I'll make the trade for
    emoji reactions.
  </p>

  <p>
    I think most humans find synchronous communication to be easier. Instant
    messaging has always had a widespread appeal that posting on forums has not.
  </p>
</div>
